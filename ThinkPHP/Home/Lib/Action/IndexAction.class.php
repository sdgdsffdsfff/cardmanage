     *** 函数名：main
     *** 参数：无
     *** 功能 ：主管理框架的显示
	****************************************/
	function main(){
		$this->assign('timer',$this->getTime());
		$this->display();
	}		

	/***************************************
     *** 函数名：bottom
     *** 参数：无
     *** 功能 ：底部界面显示
	****************************************/
	function bottom(){   
