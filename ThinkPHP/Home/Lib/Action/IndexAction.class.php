     *** 参数：无
     *** 功能 ：后台管理首页的显示
	 ****************************************/
	function index(){

		$this->display();	
	}

    /***************************************
     *** 函数名：top
     *** 参数：无
     *** 功能 ：头部的显示
	 ****************************************/
	function top(){
		//print_r($_SESSION);
		//exit();
		$this->display();
	}

	/***************************************
     *** 函数名：menu
     *** 参数：无
     *** 功能 ：左边菜单栏显示
	 ****************************************/		
	function menu(){
		$this->display();
	}

